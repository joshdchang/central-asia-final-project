        <p class="py-4 text-2xl">
          Although plov is “ubiquitous” [3] across various places, what makes Central Asian plov unique, and specifically Uzbek plov unique, is the
          sheer quantity of oil and fat. Zanca describes an Uzbek expression as saying, “A good plov is one where the oil drips to your elbow after
          you eat a handful from the platter” [4]. This expression bluntly describes how much Uzbeks value oil in their plov, an assessment I
          personally verified when eating plov at “The Art of Grill,” a Bukharan Jewish restaurant in Queens, New York.
        </p>
        <p class="py-4 text-2xl">
          So, why is oily plov so highly valued in Uzbekistan? Largely, it is to supplement a diet high in grains, mainly bread and rice [5].
          Essentially, when grains make up most of your diet, a fatty dish is not seen as an unhealthy indulgence as it is in many Western cultures,
          but rather as a part of general nutrition. To this point Zanca writes, “Uzbeks want to praise and consume fats not only in the sense of
          imbibing the commonsensical good life but also because they see it as an essential part of healthful or nutritious dining” [6]. In other words,
          the oil in plov is not just a luxury, but a necessity.
        </p>
        <p class="py-4 text-2xl">
          This brings us to the concept that ties plov, and really all food, so deeply to the earth: agriculture. Agriculture is quite literally the
          foundation of civilization, and with food playing such a large role in everyday life and traditions, its cultural impact cannot be
          overstated. While agriculture is impacted by political and social realities, it is fundamentally based on land, water, soil, and other
          physical geographic features.
        </p>
        <p class="py-4 text-2xl">
          From the very beginning, Central Asian agriculture was grain based. Spengler writes, “The earliest crops introduced into Central Asia likely
          came as a mixed package of free-threshing wheat, naked barley, and broomcorn millet, an assemblage pioneered further south along the
          northern foothills of the Central Asian mountains” [7]. Here, Spengler claims that wheat, barley, and millet — all cereal grains — were some
          of the first crops in Central Asia, which provides a good starting point for understanding their prominence. He also adds that they spread
          along the foothills and mountains, implying that topography played an important role in this.
        </p>
        <p class="py-4 text-2xl">
          From there, agriculture spread north as a result of a specific mix of geological features, especially topography and water. “The foothill
          ecotone provided a belt of arable land allowing crops from southern Central Asia to slowly become incorporated into the pastoralist system,
          already in place” [8], Spengler writes, demonstrating that the wet and fertile foothills were essential to spreading grains throughout
          Central Asia. Mountains also played a role in, acting as “buffers” to extremes and enabling the movement of grains into pastoralist
          communities [9].
        </p>
        <p class="py-4 text-2xl">
          In the modern day, agriculture in Central Asia is still tied to geographic conditions. Like anywhere, land, water, and agriculture are
          deeply intertwined. Grain, which is largely grown in the valleys of mountains and in river deltas, continues to dominate Central Asian farms
          and diets [10]. Although grain is consumed all over the world, Central Asia’s especially substantial reliance on cereal grains is a result of
          a unique set of historical and geographic circumstances. That grain agriculture, in turn, defines Uzbek cuisine, including plov. It all
          comes back to geography.
        </p>
